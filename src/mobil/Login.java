    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        PreparedStatement ps = null;
        ResultSet rs=null;
        String name=nameFeld.getText();
        String pass=String.valueOf(passFeld.getPassword());
        if (!name.isEmpty() && !pass.isEmpty())
        try {
            ps = Utils.getConnection().prepareStatement("SELECT * FROM user WHERE Name=? and Passwort=?");
            ps.setString(1, name);
            ps.setString(2, Utils.sha256(pass));
            rs=ps.executeQuery();
            if(rs.next()){
            Mobil.cl.show(Mobil.cardPanel, "Liste");     
            }else{
                JOptionPane.showMessageDialog(null, "Die Info stimmt nicht");
            }
        } catch (SQLException ex) {
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
        } catch (NoSuchAlgorithmException ex) {
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

